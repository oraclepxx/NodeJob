function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}

function multi(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

console.log("1 + 3 = " + add(1, 2));
console.log("1 - 2 = " + subtract(1, 2));
console.log("1 * 3 = " + multi(1, 2));
console.log("1 / 2 = " + divide(1, 2));
